import pytest
from call import Call


@pytest.mark.parametrize(
    "input_number,expected",
    [
        ("090-1234-5678", "819012345678"),
        ("03-1234-5678", "81312345678"),
        ("080 1234 5678", "818012345678"),
        ("0120-123-456", "81120123456"),
        ("+81-90-1234-5678", "819012345678"),
        ("09012345678", "819012345678"),
        ("03 1234 5678", "81312345678"),
        ("0312345678", "81312345678"),
        ("+81312345678", "81312345678"),
        ("(090)1234-5678", "819012345678"),
        (" (090)1234-5678 ", "819012345678"),
    ],
)
def test_to_e164(input_number, expected):
    assert Call.to_e164(input_number) == expected
